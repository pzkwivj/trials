CREATE TABLE IF NOT EXISTS STOCKS
(ID INT PRIMARY KEY AUTO_INCREMENT,
NAME VARCHAR,
DATE DATE,
OPEN NUMERIC,
HIGH NUMERIC,
LOW NUMERIC,
CLOSE NUMERIC ,
ADJCLOSE NUMERIC ,
VOLUME NUMERIC 
);

